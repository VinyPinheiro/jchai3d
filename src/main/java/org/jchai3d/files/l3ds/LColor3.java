package org.jchai3d.files.l3ds;

/**
 *
 * @author Marcos
 */
public class LColor3 {
    
    float r;
    
    float g;
    
    float b;

    public LColor3(float r, float g, float b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public LColor3() {
        r = g = b = 0;
    }
    
}
